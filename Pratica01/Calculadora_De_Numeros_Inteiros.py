#Lendo os quatros valores inteiros fornecidos pelo usuario 
A = int(input("Digite o valor do A:"))
B = int(input("Digite o valor do B:"))
C = int(input("Digite o valor do C:"))
D = int(input("Digite o valor do D:"))

#Calculo da diferenca conforme enunciado
DIFERENCA = A * B - C * D

#Exibindo o resultado
print("A formula para a diferencia é: DIFERENCA =(A * B - C * D)")
print (f"Substituindo os valores fornecidos: ({A} * {B} - {C} * {D}")
print(f"O resultado da diferença é: DIFERENCA = {DIFERENCA}")